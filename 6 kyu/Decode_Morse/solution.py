def decode(s):
    morse = {
        '.-':'a', '....' : 'h', '---' :'o', '..-' : 'u', '.----' : '1', '-....' : '6', '-...' : 'b', '..' : 'i', '.--.' : 'p', '...-' : 'v', '..---' : '2', '--...' : '7', '-.-.' : 'c', '.---' : 'j', '--.-' : 'q', '.--' : 'w', '...--' : '3', '---..' : '8', '-..' : 'd', '-.-' : 'k', '.-.' : 'r', '-..-' : 'x', '....-' : '4', '----.' : '9', '.' : 'e', '.-..' : 'l', '...' : 's', '-.--' : 'y', '.....' : '5', '-----' : '0', '..-.' : 'f', '--' : 'm', '-' : 't', '--..' : 'z', '--.' : 'g', '-.' : 'n', '' : ' '}
    final = ''
    if len(s) == 0:
        return final
    else:
      str1 = s.split(' ')

      for item in str1:
          final += morse[item]

    return final